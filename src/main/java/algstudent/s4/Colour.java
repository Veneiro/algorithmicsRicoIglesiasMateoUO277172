package algstudent.s4;

public class Colour {

	String colour = "";
	public Colour(String colour) {
		this.colour = colour;
	}
	/**
	 * @return the colour
	 */
	protected String getColour() {
		return colour;
	}
	/**
	 * @param colour the colour to set
	 */
	protected void setColour(String colour) {
		this.colour = colour;
	}
	
}
